package com.rays.basic;

public class MultiplicationTable {
	public static void main(String[] args) {
		
		int a = 13;
		
		System.out.println("multiplication of " + a + " :");
		
		for(int i = 0 ; i <= 10 ; i++ ) {
			
			System.out.println(a + " * " + i + " = "  +  a * i + "\t"); ;
			
		}
	}
}
	


