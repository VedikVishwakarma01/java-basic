package com.rays.basic;

public class SwitchCase {

	public static void main(String[] args) {

		String day = "Monday";

		switch (day) {
		case "Monday":
			System.out.println("this is first day of week");
			break;
		case "Tuesday":
			System.out.println("this is second day of week");
			break;
		case "Wednesday":
			System.out.println("this is third day of week");
			break;
		case "Thursday":
			System.out.println("this is fourth day of week");
			break;
		case "Triday":
			System.out.println("this is fifth day of week");
			break;
		case "Friday":
			System.out.println("this is sixth day of weeek");
			break;
		case "Sunday":
			System.out.println("tihs is seventh day of week");
			break;
		}
	}
}
